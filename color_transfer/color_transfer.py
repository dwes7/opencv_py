import numpy as np
import cv2

def color_transfer(source, target):

    # convert the images from RGB to L*a*b colorspace, being
    # sure to utilize the floating point data type (note: OpenCV
    # expects floats to be 32-bit, so sue that instead of 64-bit)
    source = cv2.cvtColor(source, cv2.COLOR_BGR2LAB).astype("float32")
    target = cv2.cvtColor(target, cv2.COLOR_BGR2LAB).astype("float32")



    # compute color statistics for the source and target images
    (lMeanSrc, lStdSrc, aMeanSrc, aStdSrc, bMeanSrc, bStdSrc) = image_stats(source)
    (lMeanTar, lStdTar, aMeanTar, aStdTar, bMeanTar, bStdTar) = image_stats(source)

    # subtract the means from the target image
    (l, a, b) = cv2.split(target)
    l -= lMeanTar
    a -= aMeanTar
    b -= bMeanTar

    # scale by the starndard deviations
    l = (lStdTar / lStdSrc) * l
    a = (aStdTar / aStdSrc) * a
    b = (bStdTar / bStdSrc) * b

    # clip the pixel intensities to [0, 255] if the fall outside
    # this range
    l = np.clip(l, 0, 255)
    a = np.clip(a, 0, 255)

    # merge the channels together and convert back to the RGB color
    # space, being sure to utilize the 8-bits unsigned integer data
    # type
    transfer = cv2.merge([l, a, b])
    transfer = cv2.cvtColor(transfer.astype("uint8"), cv2.COLOR_LAB2BGR)

